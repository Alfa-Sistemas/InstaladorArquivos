CREATE OR ALTER PROCEDURE ST_CUPOM_ITEM_INSERT (CODIGO VARCHAR(50), COD_POSTO_ABASTECIMENTO INTEGER, COD_CUPOM VARCHAR(50), ITEM INTEGER, COD_PRODUTO INTEGER, "UNIDADE" VARCHAR(10), QTDE NUMERIC(15,4), VALOR_UNITARIO NUMERIC(15,3), VALOR_SUBTOTAL NUMERIC(15,2), VALOR_DESCONTO NUMERIC(15,2), VALOR_ACRESCIMO NUMERIC(15,2), VALOR_TOTAL NUMERIC(15,2), "CST" VARCHAR(3), "ALIQUOTA" NUMERIC(15,2), CANCELADO INTEGER, COD_TOTALIZADOR VARCHAR(7), TAMANHO VARCHAR(10), COR VARCHAR(15), CODGRADE VARCHAR(10), CODBARRAS VARCHAR(20))
AS
begin 
    INSERT INTO CUPOM_ITEM
    (
        CODIGO, 
        COD_POSTO_ABASTECIMENTO, 
        COD_CUPOM,
        ITEM,
        COD_PRODUTO,
        UNIDADE,
        QTDE,
        VALOR_UNITARIO,
        VALOR_SUBTOTAL,
        VALOR_DESCONTO,
        VALOR_ACRESCIMO,
        VALOR_TOTAL,
        CST,
        ALIQUOTA,
        CANCELADO,
        COD_TOTALIZADOR,
        TAMANHO,
        COR,
        CODGRADE,
        CODBARRAS
    ) values (
        :CODIGO, 
        :COD_POSTO_ABASTECIMENTO, 
        :COD_CUPOM,
        :ITEM,
        :COD_PRODUTO,
        :UNIDADE,
        :QTDE,
        :VALOR_UNITARIO,
        :VALOR_SUBTOTAL,
        :VALOR_DESCONTO,
        :VALOR_ACRESCIMO,
        :VALOR_TOTAL,
        :CST,
        :ALIQUOTA,
        :CANCELADO,
        :COD_TOTALIZADOR,
        :TAMANHO,
        :COR,
        :CODGRADE,
        :CODBARRAS
    );
    
    suspend; 
end